package com.company;

public class swtich_statement {
    public static void main(String[] args) {
        
        char Char1 = 'D';
        switch (Char1){

            case 'A'  :
            System.out.println("It is A");
            break;

            case 'B':
            System.out.println("it is B");
            break;

            case 'C':
            System.out.println("it is c");
            break;

            case 'D':
            System.out.println("it is D");
            break;

            default:
            System.out.println("Abort");

        }
    }
}
